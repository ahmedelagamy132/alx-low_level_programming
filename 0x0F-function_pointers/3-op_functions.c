{
	return (a + b);
}

/**
 * op_sub - sub
 * @a: a
 * @b: b
 * Return: return
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply
 * @a: a
 * @b: b
 * Return: return
 */

int op_mul(int a, int b)
{
	return (a * b);
}

